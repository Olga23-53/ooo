#описание сотрудника
name = "Иван Иванов"#строка(имя сотрудника)
age = 23#целое число(возраст сотрудника)
salary = 70000.500#число с плавающей точкой(зарплата)
is_manager = True#логический тип(является ли менеджером)

#выводим типы данных для каждой переменной
print("Тип переменной name:", type(name))
print("Тип переменной age:", type(age))
print("Тип переменной salary:", type(salary))
print("Тип переменной is_manager:", type(is_manager))